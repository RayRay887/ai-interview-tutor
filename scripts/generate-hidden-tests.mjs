/**
 * Writes hidden test map for question generation.
 * Optionally validates against reference solutions when Python is available.
 * Run: node scripts/generate-hidden-tests.mjs
 */

import { spawnSync } from 'child_process'
import { writeFileSync } from 'fs'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'
import { featuredHiddenTests } from './hidden-tests/featured-cases.mjs'
import { referenceSolutions } from './hidden-tests/reference-solutions.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))

function splitAssignments(input) {
  const parts = []
  let current = ''
  let depth = 0
  let quote = null

  for (let i = 0; i < input.length; i += 1) {
    const ch = input[i]
    if (quote) {
      current += ch
      if (ch === quote && input[i - 1] !== '\\') quote = null
      continue
    }
    if (ch === '"' || ch === "'") {
      quote = ch
      current += ch
      continue
    }
    if ('[{('.includes(ch)) depth += 1
    if (']})'.includes(ch)) depth -= 1
    if (ch === ',' && depth === 0) {
      if (current.trim()) parts.push(current.trim())
      current = ''
      continue
    }
    current += ch
  }
  if (current.trim()) parts.push(current.trim())
  return parts
}

function parseInput(input) {
  const args = {}
  for (const assignment of splitAssignments(input)) {
    const eq = assignment.indexOf('=')
    if (eq === -1) continue
    const key = assignment.slice(0, eq).trim()
    const raw = assignment.slice(eq + 1).trim()
    args[key] = parseValue(raw)
  }
  return args
}

function parseValue(raw) {
  if (
    (raw.startsWith('"') && raw.endsWith('"')) ||
    (raw.startsWith("'") && raw.endsWith("'"))
  ) {
    return raw.slice(1, -1)
  }
  if (raw === 'true') return true
  if (raw === 'false') return false
  if (/^-?\d+$/.test(raw)) return Number(raw)
  return JSON.parse(raw)
}

function validateWithPython(slug, solution, cases) {
  const fnMatch = solution.match(/def\s+(\w+)/)
  if (!fnMatch) return

  for (const testCase of cases) {
    const args = parseInput(testCase.input)
    const expected = parseValue(testCase.output.trim())
    const argValues = Object.values(args)
    const script = `${solution}\nimport json\nresult = ${fnMatch[1]}(${argValues.map((v) => JSON.stringify(v)).join(', ')})\nprint(json.dumps(result))`
    const proc = spawnSync('python', ['-c', script], { encoding: 'utf8' })
    if (proc.status !== 0) {
      console.warn(`[hidden-tests] Skip validation for ${slug} (Python unavailable)`)
      return
    }
    const actual = JSON.parse(proc.stdout.trim())
    if (JSON.stringify(actual) !== JSON.stringify(expected)) {
      console.error(`[hidden-tests] Failed ${slug}: ${testCase.input}`)
      console.error(`  expected ${JSON.stringify(expected)}, got ${JSON.stringify(actual)}`)
      process.exit(1)
    }
  }
  console.log(`[hidden-tests] OK ${slug} (${cases.length} cases)`)
}

const pythonCheck = spawnSync('python', ['--version'], { encoding: 'utf8' })
if (pythonCheck.status === 0) {
  for (const [slug, cases] of Object.entries(featuredHiddenTests)) {
    const solution = referenceSolutions[slug]
    if (solution) validateWithPython(slug, solution, cases)
  }
} else {
  console.warn('[hidden-tests] Python not found — skipping reference validation')
}

const outPath = join(__dirname, 'hidden-tests/generated-map.mjs')
writeFileSync(
  outPath,
  `// Auto-generated by scripts/generate-hidden-tests.mjs\nexport const hiddenTestsBySlug = ${JSON.stringify(featuredHiddenTests, null, 2)}\n`,
)
console.log(`Wrote ${outPath}`)
